public class numeroLista {
    public static void main(String[] args){
        int numero1 = 1;
        int numero2 = 2;
        int numero3 = 3;
        int numero4 = 4;
        int numero5 = 5;


        int[] numeros = new int[] {numero1,numero2,numero3,numero4,numero5};
        for (int numero : numeros){
            System.out.println(numero);
        }

    }
}
